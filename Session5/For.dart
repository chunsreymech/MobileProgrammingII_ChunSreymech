void main(){
    print("Print Hello World! \n");
    for(int i=1; i<=10; i++){
        print("Hello World! \n");
    }

    print("Print odd numbers 0-9: \n");
    for (int i=0; i<10; i++){
        print(i);
    }

    print("Print even numbers 2-20: \n");
    for (int i=2; i<=20; i+=2){
        print(i);
    }
}